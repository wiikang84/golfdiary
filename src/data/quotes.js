// 랜덤 명언 가져오기
export const getRandomQuote = () => {
  const randomIndex = Math.floor(Math.random() * golfQuotes.length);
  return golfQuotes[randomIndex];
};

// 오늘의 명언 (날짜 기반)
export const getTodayQuote = () => {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24)
  );
  const index = dayOfYear % golfQuotes.length;
  return golfQuotes[index];
};

// 특정 인덱스의 명언 가져오기
export const getQuoteByIndex = (index) => {
  return golfQuotes[index % golfQuotes.length];
};

// 전체 명언 개수
export const getQuotesCount = () => {
  return golfQuotes.length;
};
